$('.icon-toggle3').click(function() {
    $(this).toggleClass('fa-chevron-down fa-chevron-up');
});

document.getElementById('toggleDiv3').addEventListener('click', function() {
    var icon = this.querySelector('.icon-toggle3');
    var collapsibleContent3 = document.getElementById('collapsibleContent3');
    
    if (collapsibleContent3.classList.contains('show')) {
        collapsibleContent3.classList.remove('show');
        icon.classList.remove('fa-chevron-up');
        icon.classList.add('fa-chevron-down');
    } else {
        collapsibleContent3.classList.add('show');
        icon.classList.remove('fa-chevron-down');
        icon.classList.add('fa-chevron-up');
    }
});

